class Fixture:

    def __init__(self, home_team, away_team, fixture_date, fixture_result, league_id, id=None):
        self.home_team = home_team
        self.away_team = away_team
        self.fixture_date = fixture_date
        self.fixture_result = fixture_result
        self.league_id = league_id
        self.id = id
